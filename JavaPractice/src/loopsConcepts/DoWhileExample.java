package loopsConcepts;

public class DoWhileExample {
	public static void main(String[] args) {
		
		int jai=7;
		do
		{
			System.out.println(jai+" SriRam");
			jai++;
		}while(jai<=12);
		
		do
		{
			System.out.println(jai+"  dasara");
			jai++;
		}while(jai<=16);
		
		do
		{
			System.out.println(jai+"   paris");
			jai++;
			
		}while(jai>=-5);
		
		
	}

}
