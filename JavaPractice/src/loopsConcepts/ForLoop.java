package loopsConcepts;

public class ForLoop {
	public static void main(String[] args) {
		for (int echo=1; echo<3; echo++)
		{
			System.out.println(echo+"  "  + " MCU");
		}
		
		System.out.println("**********************************");
		
		for(int leo = 4; leo<=8; leo=leo+1)
		{
			System.out.println(leo+"-" + "LCU");
		}
		
		System.out.println("&&&&&&&&&&&&&&&&&&&&&&&&&&");
		
		for(int jhonwick= -5; jhonwick<=5; jhonwick=jhonwick+1)
		{
			System.out.println(jhonwick+"  "+"Pennsylvania");
		}
	}

}
