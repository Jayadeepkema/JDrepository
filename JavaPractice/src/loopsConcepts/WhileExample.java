package loopsConcepts;

public class WhileExample {
	public static void main(String[] args) {
		
		int kia=1;
		while(kia<=3)
		{
			System.out.println(kia+"  "+ "kia");
			kia++;
		}
		
		while(kia<=6)
		{
			System.out.println(kia+"  Testing");
			kia++;
		}
		while(kia>=-8)
		{
			System.out.println(kia+"WarMachine");
			kia=kia-4;
		}

	}

}
