package julyJavaPractice;

public class SwitchCaseExample2 {
	public static void main(String[] args) {
		char ch = 's';

		switch (ch) {

		case 'a':
			System.out.println("nickfury");
			break;

		case 'b':
			System.out.println("Natasha");
			break;

		case 'g':
			System.out.println("CaptainAmerica");
			break;

		case 'j':
			System.out.println("IronMan/TonyStark");
			break;

		case 'k':
			System.out.println("Thor");
			break;

		case 'r':
			System.out.println("BruceBanner/Hulk");
			break;

		case 'q':
			System.out.println("BlackPanther/T'Challa");
			break;

		case 'i':
			System.out.println("WarMachine/Rhodey");
			break;

		case 'o':
			System.out.println("DoctorStrange");
			break;

		default:
			System.out.println("SpiderMan is avaliable here");
			break;

		}

	}

}
